        <h1>Biographie</h1>
	  <span class="danke-birgit">Danke Birgit für die Zusammenarbeit bei dieser Biographie</span>
	<div class="bio-text">
        <p>
            <a href="img/biographie_bild.jpg" class="bio-bild1"><img src="img/biographie_bild.jpg"></a><span class="first-letter">D</span>ie Anfänge meines Malens liegen im Jahr 1977.<br>
            Zu dieser Zeit fing ich an erste Aquarellkurse bei der VHS zu besuchen.<br>
            Das Sanfte, Zerfließende der Aquarellmalerei, das Fehlen harter Linien und klarer Konturen hatten schon damals ihren besonderen Reiz auf mich.<br>
            Meine Motive waren damals durch meine Liebe zur Natur geprägt.<br>
            Landschaften und Blumen belebten meine Bilder.<br>
            Letztere waren dann später auch bei der Seidenmalerei bildgebend.<br>
<br>
            Lange Zeit stand der dekorative Gedanke beim Malen meiner Bilder im Vordergrund, bis ich - duch den Malstil einer guten Freundin inspiriert - merkte, dass mir meine Bildaussagen nicht mehr genügten.<br>
            Langsam wurde der Wunsch in mir immer größer mich von konventionellen Bildgebungen zu lösen. Es drängte mich der Gedanke Bilder, die meine ureigene Sprache sprechen, zu gestalten, einen mir eigenen Stil zu entwickeln. So begann die Suche nach einem Kunstlehrer, unter dessen Anleitung ich mir erhoffte neue Wege zu beschreiten.<br>
            Seit 2005 besuche ich nun die Kunstkurse von Reiner Pfeiffer im Institut zur Beibehaltung ästhetischer Gewohnheiten in Kreuzwertheim, zusätzlich, seit 2007 regelmäßig einmal im Jahr die einwöchige Kunstakademie unter Leitung von Reiner Pfeiffer in Alixan (Südfrankreich).<br>
            Doch es sollte noch eineinhalb Jahre dauern, bis es mir gelang mich von meinem Malstil der dekorativen Wohnzimmerkunst zu lösen.<br>
            Bei meiner ersten Sommerakademie 2007 in Alixan fühlte ich mich so provoziert, dass ich das erste Mal begann, geleitet von diesem Gefühl der Provokation, einfach aus mir heraus zu malen.<br>
            Gedanken wurden ausgeschaltet und durch meine Fantasie beflügelt entstanden erste dämonenhafte Wesen mit einer ganz eigenen Strichführung.<br>
            Hieraus entwickelte sich dann zunehmend der mir heute eigene Malstil.<br>
            Ich entdeckte mein Faible fürs Fantastische und Groteske, was sich bis heute durch meine Werke zieht.<br>
<br>
            <a href="img/bio_rosi.jpg" class="bio-bild2"><img src="img/bio_rosi.jpg"></a>Wenn ich die Findung meiner Bilder beschreiben sollte, würden es die Worte "Die Bilder drängen sich mir auf, ich finde sie" wohl am besten beschreiben.<br>
            Dem Unterbewusstsein Bilder zu entlocken, Ausflüge ins Fantastische jenseits von Regeln und kühlem Wissen bestimmen meinen Stil.<br>
            Das Wozu und Warum bleiben hierbei meist unbeantwortet.<br>
<br>
            So scheinen meine Bilder in gewisser Weise der Spiegel meiner Seele zu sein, geben sie doch meine Grundhaltung zum Leben an sich wieder.<br>
            Zum einen sind da die Tiere, eine Urform von Lebewesen, durch Äußeres nicht wandelbar, Leben in freier Wildbahn, in seiner Ungezähmtheit beherrschen ihr Naturell.<br>
            Zum anderen ist da der Mensch. Meist dargestellt in seiner Nacktheit, welche den Blick auf das Wesentliche erlaubt, keine Reduzierung auf Äußerlichkeiten zulässt, diesem Diktat nicht unterworfen ist.<br>
            Im Besonderen das tolle Weib, nicht reduziert auf das rein Sexuelle, sondern die Möglichkeit Frausein zu leben, Üppiges zuzulassen, Sinnlichkeit zeigen zu dürfen, Feminität und nicht Androgynität.<br>
            Zu guter Letzt das zauberhaft Mystische, Imaginäre meiner Bilder, die Möglichkeit den Zwängen des Alltags zu entfliehen als Einladung an das Glück des für mich wahren Lebens.<br>
            Sollte ich persönlich meine Bilder beschreiben, so fände ich mich in folgendem Zitat von Jose Ortega Y Gasset wieder:<br>
            <br>
	  </p>
	  <p>
            <b>"Das Kunstwerk ist eine imaginäre Insel, die rings von Wirklichkeit umbrandet ist"</b><br>
        </p>
